<div id="ablef"><h2 style="margin-left:10px;" align="left">Citation</h2><p style="opacity:1; margin-left:20px; margin-right:15px;" align="justify">A Citation is a reference to a published or unpublished source .<br/> More precisely, a citation is an abbreviated alphanumeric expression  embedded in the body of an intellectual work that denotes an entry in the bibliographic references section of the work for the purpose of acknowledging the relevance of the works of others to the topic of discussion at the spot where the citation appears.<br/> Generally the combination of both the in-body citation and the bibliographic entry constitutes what is commonly thought of as a citation . References to single, machine-readable assertions in electronic scientific articles are known as nanopublications, a form of microattribution.<br/>
Citation has several important purposes: to uphold intellectual honesty to attribute prior or unoriginal work and ideas to the correct sources, to allow the reader to determine independently whether the referenced material supports the author's argument in the claimed way, and to help the reader gauge the strength and validity of the material the author has used.</p></div>
